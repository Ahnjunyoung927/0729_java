package com.kh.operator;

public class D_LogicalNagation {
	/*
	 * 논리(값) 부정 연산자 : 논리 값(true, false)을 반대로 바꿔주는 연산자
	 * 
	 * [ 표현법 ] !논리값;
	 * 
	 * 
	 * 
	 * 
	 */
	
	public void method1( ) {
		
		System.out.println(!true);
		// => !를 붙여 반대로 변환 
		// => !! 느낌표를 두개 붙이면 반전의 반전이라 원래대로 돌아옴
		
		
	}
	
	

}
